package com.xxg.vxinv.common.util.ybeans;

public class Proxys {
    Short id;
    String proxy_addr;
    int proxy_port;


    public Short getId() {
        return id;
    }

    public void setId(Short id) {
        this.id = id;
    }

    public String getProxy_addr() {
        return proxy_addr;
    }

    public void setProxy_addr(String proxy_addr) {
        this.proxy_addr = proxy_addr;
    }

    public int getProxy_port() {
        return proxy_port;
    }

    public void setProxy_port(int proxy_port) {
        this.proxy_port = proxy_port;
    }
}
